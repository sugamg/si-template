# MAGIC The project is structured into the following folders:
# MAGIC - **Inputs**: contains the configuration notebook in which all of the user-defined parameters can be configured
# MAGIC - **Notebooks**: contains all of the individual notebooks (sub-notebooks) which are called in the "main" notebook
# MAGIC - **Utils**: contains all of the functions used in the above notebooks
# MAGIC - **Tests**: intended to contain any unit test code
# MAGIC - **Main**: The main notebook to runs all of the relevant sub-notebooks. This is the only notebook that needs to be run to execute the whole pipeline. To give you control over which sub-notebooks are run (mainly for testing purposes) this can be set in the config notebook. 

